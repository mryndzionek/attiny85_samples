static uint16_t rnd(uint16_t range)
{
    static uint16_t lfsr = 0xACE1u;

    unsigned lsb = lfsr & 1;
    lfsr >>= 1;
    if (lsb)
    {
        lfsr ^= 0xB400u;
    }

    return lfsr % range;
}

bool snd_get_sample(uint8_t *sample)
{
    static uint16_t phase = 0;
    static uint16_t f = 400;
    static uint16_t c = 0;
    static uint16_t n = 80;
    static uint16_t i = 0;
    static uint16_t len = 2000;

    if (c++ == n)
    {
        c = 0;
        n = (250 - 100) + rnd(201);
        f = 600 + rnd(3400);
    }

    fix16_t v = pgm_read_word(&sine_tab[phase >> 6]);
    *sample = ((v / 64) + 128);
    phase += f * 8;

    if (++i == len)
    {
        i = 0;
        len = 2000 + rnd(3200);
        return true;
    }
    else
    {
        return false;
    }
}

