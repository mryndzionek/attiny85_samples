static uint16_t rnd(uint16_t range)
{
    static uint16_t lfsr = 0xACE1u;
    unsigned lsb = lfsr & 1;
    lfsr >>= 1;
    if (lsb)
    {
        lfsr ^= 0xB400u;
    }

    return lfsr % range;
}

bool snd_get_sample(uint8_t *sample)
{
    static uint16_t p = 0;

    if (p == sizeof(wavtable))
    {
        p = 0;
        return true;
    }
    else
    {
        *sample = pgm_read_byte(&wavtable[p++]);
        return false;
    }
}

void snd_wait(void)
{
    static uint8_t count = 4;
    static uint8_t c = 0;

    if (++c == count)
    {
        c = 0;
        count = 2 + rnd(4);
        annoy_deep_sleep(2 + rnd(10));
    }
    else
    {
        _delay_ms(200);
    }
}
